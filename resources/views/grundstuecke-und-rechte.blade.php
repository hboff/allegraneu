<h2>Sanierungsgebiete</h2>
<p>Sanierungsgebiete sind festgelegte Bereiche, in denen eine gemeinde städtebauliche Sanierungsmaßnahmen durchführen möchte.</p>
<p>Dies erfolgt im Regelfall, da diese Bereiche funktionale Schwächen aufweisen. Die Sanierungsmaßnahmen haben das Ziel bauliches Erbe zu sichern aber gleichzeitig die Wohn- und Arbeitsbedingungen zu verbessern. Außerdem soll das Gebiet an den wirtschaftlichen Wandel angepasst werden.</p>
<h2>Baulasten</h2>
<p>Baulasten regeln Verpflichtungen, die der Grundstückseigentümer gegenüber der Baubehörde hat, aber auch Dinge, die dieser erfüllen muss, zu unterlassen hat oder dulden muss.</p>
<p>Die Baulasten eines Grundstücks lassen sich im Baulastenverzeichnis der Baubehörde finde. Die häufigsten Baulasten sind Erschließungsbaulasten und Abstandflächenbaulasten. Bei diesen müssen bestimmte Bereiche des Grundstücks freigehalten werden, damit ein anderes Grundstück erschlossen werden kann oder die Mindestabstände zu anderen Gebäuden eingehalten werden können.</p>
<h2>Nießbrauchrecht</h2>
<p>Durch das Nießbrauchrecht überträgt der Eigentümer einer Immobilie seine Rechte bezüglich des Bewohnens und der wirtschaftlichen Nutzung auf eine andere Person. Diese darf die Immobilie nutzen oder die Miete daraus erhalten, ohne dass sie selbst Eigentümer ist. Das Recht eines Nutznießers ist nicht vererbbar.</p>
<h2>Erbbaurecht</h2>
<p>Das Gebäude gehört dem Erbbauberechtigten, während das Grundstück beim Eigentümer bleibt.</p>




